 #整数，小数，字符串直接的转换；
 #str()函数：用于将一个变量转化成字符串类型的数据

a = 10
print(type(a))
a1=str(a)
print('a转换之后的类型：',type(a1))

b =9.88
print(type(b))
b1=str(b)
print('b转换之后的类型：',type(b1))

#int()函数：将一个纯整数类型的字符串转化成整数类型的数据。
c ='20'
print(type(c))
c1=int(c)
print('c转化之后的类型',type(c1))

#float()函数：将一个纯数字类型的字符串转化成小数类型的数据。
d='99.80'
print(type(d))
d1=float(d)
print('d1转换之后的类型',type(d1))



