#通常情况下,一个网页可以分为三个部分:HTML,CSS,JavaScript,如果把网页比作一个人的话:HTML相当于一个人的骨架,CSS相当于皮肤,JavaScripit相当于肌肉,这三种的结合才能形成一个完善的网页.
#html是超文本标记语言,他不能算作一门编程语言,但是它是一种标记语言,HTML使用标记标签来描述网页,网页包括文字,图片,按钮,视频等各种元素,最基本的机构就是HTML
#html定义了网页的结构,但是html页面并不美观,为了让网页变得更好看,所以产生了css
#html和css虽然给用户提供了美观的页面,但是这样的页面缺少交互性,于是,便使用javascript来给页面添加一些交互和动画效果,如下载进度条,提示框,轮播图等等,javascript也是一门编程语言,和python一样属于脚本语言的范畴