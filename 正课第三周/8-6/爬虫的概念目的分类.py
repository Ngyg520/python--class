#2.爬虫的目的
'''
1.获取数据,用于填充公司定的数据库.
2.通过爬虫数据,制作搜索引擎.
3.通过爬虫数据,做数据采集和数据分析.

'''

#3.反爬虫的目的
'''
1.针对一些初级爬虫程序,简单除暴,不会考虑服务器的压力,会导致服务器瘫痪.
2.针对一些失控的爬虫,爬虫的数据量很庞大,忘记关闭爬虫程序的.

'''
